#include <unistd.h>

/**
 * @brief 将一个三位数字的字符串转换为一个整数。
 * 
 * 该函数将字符串 `str` 中从位置 `i` 开始的三位数字字符转换为一个整数。
 * 字符串中的字符必须是数字字符（'0' 到 '9'），并且该函数假设字符串包含三位数字（例如 "123"）。
 * 如果字符不是数字，行为未定义。
 * 
 * @param str 输入的字符串，其中包含至少三个字符。
 * @param i 起始索引，从该位置开始转换三位数字。
 * 
 * @return 返回转换后的整数，表示 `str[i]` 到 `str[i+2]` 中的数字。若字符串长度不足三位，结果可能不准确。
 */
int my_stroi(char *str, size_t i);

/**
 * @brief 将一个三位八进制数转换为十进制数。
 * 
 * 该函数将一个三位八进制数（以整数形式表示）转换为十进制数。输入的整数 `n` 表示一个三位八进制数，函数将其转换为相应的十进制值。
 * 
 * @param n 输入的三位八进制整数。
 * 
 * @return 返回 `n` 转换为十进制后的整数。
 */
int octal_to_dec(int n);